lista01 = [2.0, 3.5, 4.7]
print(lista01)

lista02 = [1, 5, 9, 11, 15]
print(lista02)

""" len() """
# len() puxa o tamanho do vetor/lista (quantidade de itens contidos nela)
# pode ser utilizado em qualquer tipo de dado (int, float, str...)
print("\nlen( ):")
print(f'Tamano da lista 01 -> {len(lista01)}')
print(f'Tamano da lista 02 -> {len(lista02)}')

""" sum() """
# sum() -> soma todos os itens presentes na lista/vetor
#pode ser usado apenas em tipos de dados numÃ©ricos 
print("\nsum( ):")
print(f'Soma da lista 01 -> {sum(lista01)}')
print(f'Soma da lista 02 -> {sum(lista02)}')

""" max() """
# max() -> retorna o elemento de valor mÃ¡ximo da lista/vetor
#pode ser usado apenas em tipos de dados numÃ©ricos 
print("\nmax( ):")
print(f'Maior valor da lista 01 -> {max(lista01)}')
print(f'Maior valor da lista 02 -> {max(lista02)}')

""" min() """
# min() -> retorna o elemento de menor valor da lista/vetor
#pode ser usado apenas em tipos de dados numÃ©ricos 
print("\nmax( ):")
print(f'Menor valor da lista 01 -> {min(lista01)}')
print(f'Menor valor da lista 02 -> {min(lista02)}')